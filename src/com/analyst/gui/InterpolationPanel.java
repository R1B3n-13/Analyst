    public static void setValue(double num, int i, int j) {
        Formatter formatter = new Formatter();
        if (j == 0) {
            formatter.format("%.0f", num);
        } else {
            formatter.format("%.5f", num);
        }
        resTable.getModel().setValueAt(formatter, i, j);
    }

